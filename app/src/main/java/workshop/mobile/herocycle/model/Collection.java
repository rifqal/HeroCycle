package workshop.mobile.herocycle.model;

public class Collection {
    String clcDate, clcDesc, clcPrice, clcStatus, itemID, itemImage, reqDate, userID, weight;

    public Collection() {}

    public String getClcDate() {
        return clcDate;
    }

    public void setClcDate(String clcDate) {
        this.clcDate = clcDate;
    }

    public String getClcDesc() {
        return clcDesc;
    }

    public void setClcDesc(String clcDesc) {
        this.clcDesc = clcDesc;
    }

    public String getClcPrice() {
        return clcPrice;
    }

    public void setClcPrice(String clcPrice) {
        this.clcPrice = clcPrice;
    }

    public String getClcStatus() {
        return clcStatus;
    }

    public void setClcStatus(String clcStatus) {
        this.clcStatus = clcStatus;
    }

    public String getItemID() {
        return itemID;
    }

    public void setItemID(String itemID) {
        this.itemID = itemID;
    }

    public String getItemImage() {
        return itemImage;
    }

    public void setItemImage(String itemImage) {
        this.itemImage = itemImage;
    }

    public String getReqDate() {
        return reqDate;
    }

    public void setReqDate(String reqDate) {
        this.reqDate = reqDate;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }
}
